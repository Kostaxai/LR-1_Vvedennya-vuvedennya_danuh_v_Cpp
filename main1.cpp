#include <iostream>  // бібліотека для вводу-виводу
#include <cmath>     // бібліотека для математичних функцій (pow)
#include <locale>    // для встановлення локалі
using namespace std;

int main() {
    setlocale(LC_ALL, ""); // встановлюємо локаль для коректного відображення українських символів

    // =====================
    // Begin 17    // =====================
    cout << "========================\n";
    cout << "Виконуємо Begin 17: Обчислення функції y = 4*(x-3)^6 - 7*(x-3)^3 + 2\n";
    cout << "========================\n";

    double x1, y1;                 // оголошуємо змінні для Begin 17
    cout << "Введіть x: ";         // просимо користувача ввести значення x
    cin >> x1;                      // зчитуємо значення x

    // обчислюємо значення функції
    y1 = 4 * pow((x1 - 3), 6) - 7 * pow((x1 - 3), 3) + 2;
    cout << "Значення функції y = " << y1 << endl << endl; // виводимо результат

    // =====================
    // Begin 26
    // =====================
    cout << "========================\n";
    cout << "Виконуємо Begin 26: Розрахунок шляху човна\n";
    cout << "========================\n";

    double V, U, T1, T2, S;         // оголошуємо змінні для Begin 26
    cout << "Введіть швидкість човна в стоячій воді V (км/год): ";
    cin >> V;                        // зчитуємо швидкість човна
    cout << "Введіть швидкість течії річки U (км/год, U < V): ";
    cin >> U;                        // зчитуємо швидкість течії
    cout << "Введіть час руху човна по озеру T1 (год): ";
    cin >> T1;                       // зчитуємо час руху по озеру
    cout << "Введіть час руху човна проти течії T2 (год): ";
    cin >> T2;                       // зчитуємо час руху проти течії

    // обчислюємо шлях човна: шлях = швидкість * час
    // при русі проти течії швидкість зменшується на швидкість течії
    S = V * T1 + (V - U) * T2;      
    cout << "Загальний шлях човна S = " << S << " км" << endl << endl; // виводимо результат

    // =====================
    // Begin 46
    // =====================
    cout << "========================\n";
    cout << "Виконуємо Begin 46: Конвертація гривень у долари\n";
    cout << "========================\n";

    double hryvnias, rate, dollars;   // оголошуємо змінні для Begin 46
    cout << "Введіть суму в гривнях: ";
    cin >> hryvnias;                  // зчитуємо суму в гривнях
    cout << "Введіть курс обміну (грн/$): ";
    cin >> rate;                       // зчитуємо курс обміну

    dollars = hryvnias / rate;         // обчислюємо еквівалент у доларах
    cout << "Еквівалент у доларах = " << dollars << " $\n"; // виводимо результат

    return 0;                          // завершення програми
}
